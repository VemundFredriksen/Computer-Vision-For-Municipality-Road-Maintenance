export default function findCorrectMarker(object, workOrders) {
  const obj = workOrders.filter((item) => isSubset(object, item));
  if (obj.length !== 0) {
    return greenIcon;
  }
  if (object.responsible === 'statens vegvesen') {
    return greyIcon;
  }
  if (object.work_order) {
    return yellowIcon;
  }
  if (object.approved) {
    return blueIcon;
  }
  return redIcon;
}
